export function getACTPercentileContext(percentile: number): { label: string; color: string; description: string } {
  if (percentile >= 99) return { label: 'Exceptional', color: '#34d399', description: 'Top 1% of all ACT test takers nationwide' };
  if (percentile >= 95) return { label: 'Outstanding', color: '#34d399', description: `Top ${100 - percentile}% of all ACT test takers` };
  if (percentile >= 90) return { label: 'Excellent',   color: '#06b6d4', description: `Top ${100 - percentile}% of all ACT test takers` };
  if (percentile >= 75) return { label: 'Above average', color: '#06b6d4', description: `Better than ${percentile}% of test takers` };
  if (percentile >= 50) return { label: 'Average',     color: '#f59e0b', description: `Better than ${percentile}% of test takers` };
  if (percentile >= 25) return { label: 'Below average', color: '#f59e0b', description: `Better than ${percentile}% of test takers` };
  return { label: 'Needs improvement', color: '#ef4444', description: `Better than ${percentile}% of test takers` };
}

